#include <stdio.h>
/**********************************************/
void Swap_Ptr(int** x, int** y);
/**********************************************/
int main()
{
    int a = 5, b = 6, *x, *y;
    x = &a;
    y = &b;
    Swap_Ptr(&x, &y);
    printf("%d %d", *x, *y);
    return 0;
}
/**********************************************/
void Swap_Ptr(int** x, int** y){
    int* tmp;
    tmp = *x;
    *x = *y;
    *y = tmp;
}
